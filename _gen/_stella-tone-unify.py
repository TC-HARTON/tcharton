def transform(p: pathlib.Path) -> bool:
    s = p.read_text(encoding="utf-8")
    orig = s
    for old, new in REPLACEMENTS:
        s = s.replace(old, new)
    # 二重スペース等の整形
    s = re.sub(r"  +", " ", s)
    if s != orig:
        p.write_text(s, encoding="utf-8")
        return True
    return False

